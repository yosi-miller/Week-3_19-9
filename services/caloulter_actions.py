def calculator_atr(assist, turnover):
    """
    :param assist: Number of assists made
    :param turnover: Number of turnovers made
    :return: The Assist-to-Turnover Ratio (ATR) calculated as the ratio of assists to turnovers
    """
    atr_cal_result = assist / turnover
    return atr_cal_result


